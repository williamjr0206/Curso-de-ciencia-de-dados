texto = "William Antonio de Azevedo Junior"

print(texto[0:7])  # esvreve do caractere 0 ao 6
print(texto[8:15]) # escreve do caractere 8 ao 14
print(texto[:7])  # escreve do início ao caractere 6
print(texto[15:]) # escreve do caractere 15 até o final
print(texto[:]) # escreve a string completa
print(texto[::2]) # escreve a string completa, pulando de 2 em 2
print(texto[:-1]) # escreve a string completa de trás para frente
print(texto[15::-1]) # escreve do caractere 15 até o início, de trás para frente 
